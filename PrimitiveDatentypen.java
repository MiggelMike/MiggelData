package variablen;

public class PrimitiveDatentypen {

	public static void main(String[] args) {
		
		/*
		  	Zahlen-Typen:		 
		 */
		
		byte b = 2;		// 8 bit 			[-128 bis 127]
		short s;		// 16 bit
		int i;			// 32 bit
		long lo;		// 64 bit
		
		System.out.println( Integer.MAX_VALUE );
		System.out.println( Long.MAX_VALUE );
		
		float f;		// 32 bit
		double d;		// 64 bit
		
		/*
		 	Zeichentyp
		 */
		char ch = 97;	// 16 bit, nicht negativ
		System.out.println(ch);

		/*
		 	boolean
		 */
		boolean var = true;	// oder false
		
	}
	
}
